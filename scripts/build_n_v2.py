def main() -> int:
    inp = json.loads(INPUT.read_text(encoding="utf-8"))
    inputs = inp["candidates"]

    out_candidates = []
    scored, unscored = 0, 0
    for c in inputs:
        code = c["code"]
        n_comm = N.get(code)
        if n_comm:
            scored += 1
        else:
            unscored += 1
            n_comm = None
        out_candidates.append({
            "code": code,
            "name": c["name"],
            "market": c["market"],
            "market_cap_eok": c["market_cap_eok"],
            "current_price": c["current_price"],
            "c_score": c["c_score"],
            "c_score_tier": c["c_score_tier"],
            "n_commentary": n_comm,
        })

    # 점수 내림차순 정렬 (점수 없으면 끝으로)
    def sort_key(c):
        nc = c.get("n_commentary")
        if not nc:
            return (1, 0, 0)
        ts = nc["total_score"]
        rev = nc["scores"]["revenue_contribution"]["value"]
        return (0, -ts, -rev)

    out_candidates.sort(key=sort_key)

    # 통계
    tier_counts = {"A": 0, "B": 0, "C": 0, "D": 0, "미조사": 0}
    for c in out_candidates:
        nc = c.get("n_commentary")
        if not nc:
            tier_counts["미조사"] += 1
        else:
            tier_counts[nc["tier"]] += 1

    output = {
        "generated_at": TODAY,
        "input_track": "C 게이트 통과 (passesCGate)",
        "c_input_total": len(inputs),
        "scored_count": scored,
        "unscored_count": unscored,
        "scoring_version": "v2_30pt_product_only",
        "score_axes": {
            "competitive_advantage_max": 15,
            "revenue_contribution_max": 10,
            "sector_impact_max": 5,
        },
        "tier_cutoffs": {"A": 24, "B": 18, "C": 10, "D": 0},
        "tier_counts": tier_counts,
        "candidates": out_candidates,
    }

    OUTPUT.write_text(json.dumps(output, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"저장: {OUTPUT.relative_to(ROOT)}")
    print(f"채점 {scored} / 미조사 {unscored} = 총 {len(inputs)}")
    print(f"등급 분포: {tier_counts}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
